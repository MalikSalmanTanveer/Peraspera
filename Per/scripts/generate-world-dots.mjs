function parsePath(d) {
  const points = [];
  for (const token of d.split(/\s+/)) {
    if (token === 'Z' || token === 'z') continue;
    const cmd = token[0];
    if (cmd !== 'M' && cmd !== 'L') continue;
    const [x, y] = token.slice(1).split(',').map(Number);
    points.push([x, y]);
  }
  return points;
}

function pointInPolygon(x, y, polygon) {
  let inside = false;
  for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
    const [xi, yi] = polygon[i];
    const [xj, yj] = polygon[j];
    const intersect =
      yi > y !== yj > y && x < ((xj - xi) * (y - yi)) / (yj - yi + Number.EPSILON) + xi;
    if (intersect) inside = !inside;
  }
  return inside;
}

const landPolygons = LAND_PATHS.map(parsePath);
const highlightPolygons = HIGHLIGHT_PATHS.map(parsePath);

const dots = [];

for (let row = 0; row < ROWS; row++) {
  for (let col = 0; col < COLS; col++) {
    const x = ((col + 0.5) / COLS) * VIEW_W;
    const y = ((row + 0.5) / ROWS) * VIEW_H;
    const onLand = landPolygons.some((poly) => pointInPolygon(x, y, poly));
    if (!onLand) continue;
    const highlighted = highlightPolygons.some((poly) => pointInPolygon(x, y, poly));
    dots.push([
      Math.round((col / COLS) * 1000) / 10,
      Math.round((row / ROWS) * 1000) / 10,
      highlighted ? 1 : 0,
    ]);
  }
}

